# ### **결혼 준비 로드맵 및 문제점 정리**

# ### **1. 결혼 준비의 주요 단계**

# 1. **정보 수집 및 초기 계획**
#     - 네이버 카페(대형 카페)에서 시작.
#     - 카페에서 웨딩홀, 플래너, 드레스 등 후기와 정보를 확인.
#     - 웨딩박람회 일정 확인 및 참여.
# 2. **웨딩플래너 상담 및 계약**
#     - 플래너와 상담하여 스튜디오, 드레스, 메이크업(스드메) 패키지 선택.
#     - 웨딩홀 일정 및 예약 조율.
#     - 플래너를 통해 결혼 준비의 주요 일정을 관리.
# 3. **스드메(스튜디오, 드레스, 메이크업) 준비**
#     - 드레스 피팅 및 선택: 제한된 드레스 수로 인해 선택 폭이 좁음.
#     - 촬영 및 본식 준비.
# 4. **기타 준비**
#     - 모바일 및 종이 청첩장 제작.
#     - 신랑 정장, 현악 연주, BGM 등 세부 사항 결정.

# ### **2. 단계별 문제점**

# 1. **정보 수집 및 초기 계획**
#     - 정보의 비대칭성: 대형 카페에 업체 광고가 많아 객관적인 후기 구분 어려움.
#     - 웨딩홀 정보 수집 및 예약 어려움:
#         - 전화 문의와 주말 상담 일정 조율의 번거로움.
#         - 웨딩홀 탐방 및 상담 과정이 비효율적.
# 2. **웨딩플래너 상담 및 계약**
#     - 카페 가입 조건으로 플래너와 1:1 연결되지만, 주체적으로 비교·선택하기 어려움.
#     - 플래너가 추천한 업체를 벗어나 추가 선택 옵션 부족.
# 3. **스드메 준비**
#     - 드레스 피팅 제한:
#         - 드레스 피팅 비용 부담(5만 원/회).
#         - 업계의 폐쇄성으로 드레스 디자인 선택권 제한.
#     - 추가 비용 문제: 촬영 원본 제공 및 인화에 따른 추가 비용 발생.
# 4. **기타 준비**
#     - 청첩장, 신랑 정장, 식장 내 세부 연출 등 세부 사항 처리 과정이 복잡.
#     - 예산 초과 우려:
#         - 기본 패키지 외 추가 선택 사항으로 비용 부담 증가.

# ### **3. 로드맵 개선 방향**

# 1. **정보 수집 플랫폼 개선**
#     - 객관적인 후기 검증 시스템 도입.
#     - AI 기반 추천: 사용자 취향에 맞는 웨딩홀, 플래너, 드레스 추천.
# 2. **웨딩플래너 기능 혁신**
#     - 플래너 연결 전 다수의 옵션을 비교할 수 있는 인터페이스 제공.
#     - AI 웨딩플래너:
#         - 사용자의 취향, 예산에 맞는 플래너 및 업체 추천.
#         - 스케줄링 자동화.
# 3. **스드메 경험 개선**
#     - 드레스 피팅 가상 체험:
#         - AI 기반의 3D 피팅 시뮬레이션 제공.
#     - 추가 비용 투명화:
#         - 패키지별 상세 비용과 옵션별 추가 비용 명시.
# 4. **기타 준비 통합 관리**
#     - 청첩장, 식장 세부 연출 등을 통합 관리할 수 있는 AI 도우미 기능.
#     - 예상 예산 초과 방지:
#         - 실시간 예산 관리 기능 제공.